package j07_반복;



public class Loop5 {
    public static void main(String[] args) {

        int n=5;

        for(int i=0; i<n; i++) {
            System.out.println(i + 1);
        }


        //for은 순차적으로 증가시킬 때 사용 while은 해당 조건이 되었을 때 멈출 때 사용


        int i = 0;
        while(i < n){
            System.out.println(i+1);
            i++;

    }


    }
}








